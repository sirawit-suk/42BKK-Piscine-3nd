int	ft_str_is_lowercase(char *str)
{
	int	status;

	status = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			status = 0;
		}
		str++;
	}
	return (status);
}
