int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	num;

	num = 0;
	while ((*s1 != '\0' || *s2 != '\0') && num < n)
	{
		if (*s1 < *s2)
		{
			return (-1);
		}
		else if (*s1 > *s2)
		{
			return (1);
		}
		s1++;
		s2++;
		num++;
	}
	return (0);
}
