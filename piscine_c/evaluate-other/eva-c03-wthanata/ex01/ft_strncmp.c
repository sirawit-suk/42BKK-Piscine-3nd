int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				z;

	i = 0;
	if (n == 0)
		return (0);
	while (((i + 1) < n) && (s1[i] == s2[i]))
	{
			i++;
	}
	z = s1[i] - s2[i];
	return (z);
}
