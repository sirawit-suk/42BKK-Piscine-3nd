int	ft_str_is_alpha(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < 'A' || str[cnt] > 'z')
			return (0);
		if (str[cnt] > 'Z' && str[cnt] < 'a')
			return (0);
		cnt++;
	}
	return (1);
}
