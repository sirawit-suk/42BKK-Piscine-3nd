int	ft_str_is_uppercase(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < 'A' || str[cnt] > 'Z')
			return (0);
		cnt++;
	}
	return (1);
}
