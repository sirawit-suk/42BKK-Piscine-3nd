int	ft_str_is_printable(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < ' ' || str[cnt] > '~')
			return (0);
		cnt++;
	}
	return (1);
}
